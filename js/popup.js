const popupClose = document.querySelector(".popup-close-btn");
const portfolioPopup = document.querySelector(".portfolio-popup");

// for popup focus
let focusableElements = portfolioPopup.querySelectorAll('.popup-close-btn, .portfolio-popup-btn');

document.addEventListener('focus', (event) => { 
  if(portfolioPopup.style.visibility = "visible" && !Array.from(focusableElements).includes(event.target))
  Array.from(focusableElements)[0].focus();
}, true);


function showPopup(id) {
  const selectedData = data.find((entry) => entry.id === id);

  // Update the content of the elements using their IDs
  document.querySelector("#title--intro").textContent = selectedData.title;
  document.querySelector("#subtitle--intro").textContent =
    selectedData.subtitle;
  document.querySelector("#img--intro").src = selectedData.image;
  document.querySelector("#img--intro").alt = selectedData.title;
  document.querySelector("#popup-description").textContent =
    selectedData.description;
  document.querySelector(
    "#popup-tech"
  ).textContent = ` ${selectedData.technologies.join(", ")}`;
  document.querySelector("#popup-web-link").href = selectedData.link;
  document.querySelector("#popup-github-link").href = selectedData.github;

  portfolioPopup.style.visibility = "visible";
  document.body.classList.add("body-scroll-hide");
  document.body.style.overflowY = "hidden";
}

// for close popup by close button
popupClose.addEventListener("click", () => {
  portfolioPopup.style.visibility = "hidden";
  document.body.classList.remove("body-scroll-hide");
  document.body.style.overflowY = "auto";
});

// for close popup by escape key
window.onkeydown = function( event ) {
  if ( event.keyCode == 27 ) {
    portfolioPopup.style.visibility = "hidden"
    document.body.style.overflowY = "auto";
    document.body.classList.remove("body-scroll-hide");
  }
};



